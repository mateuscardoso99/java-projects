package model;

public class Vendedor {
    private String nome;
    private String matricula;

    public String getNome(){
        return nome;
    }
    public void setNome(String nome){
        this.nome = nome;
    }
    public String getMatricula(){
        return matricula;
    }
    public void setMatricula(String m){
        matricula = m;
    }
}
